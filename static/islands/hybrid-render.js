// Hybrid Rendering - Mix static and dynamic content
// Mimics Astro's hybrid rendering mode (SSG + SSR)
export default function initHybridRendering(element) {
  const staticContent = element.querySelector('[data-static]');
  const dynamicSlots = element.querySelectorAll('[data-dynamic]');
  
  // Keep static content as-is (already rendered by Hugo)
  if (staticContent) {
    staticContent.style.cssText = 'opacity: 1; transition: opacity 0.3s;';
  }
  
  // Load dynamic slots on demand
  dynamicSlots.forEach(slot => {
    const sourceUrl = slot.dataset.source;
    const fallback = slot.innerHTML; // Keep fallback content
    
    slot.innerHTML = '<span style="color: #666;">Loading...</span>';
    slot.style.cssText = 'min-height: 50px; padding: 8px; background: #f9f9f9; border-radius: 4px;';
    
    const loadDynamic = async () => {
      try {
        const response = await fetch(sourceUrl);
        if (!response.ok) throw new Error('Failed to load');
        
        const html = await response.text();
        slot.innerHTML = html;
        slot.style.background = '#e8f5e9';
        
        setTimeout(() => {
          slot.style.transition = 'background 0.5s';
          slot.style.background = 'transparent';
        }, 1000);
      } catch (err) {
        console.error('Dynamic load failed:', err);
        slot.innerHTML = fallback || '<span style="color: #f44336;">Failed to load dynamic content</span>';
      }
    };
    
    // Load based on trigger strategy
    const trigger = slot.dataset.trigger || 'visible';
    
    if (trigger === 'immediate') {
      loadDynamic();
    } else if (trigger === 'visible') {
      const observer = new IntersectionObserver((entries) => {
        if (entries[0].isIntersecting) {
          loadDynamic();
          observer.disconnect();
        }
      }, { rootMargin: '100px' });
      observer.observe(slot);
    } else if (trigger === 'interaction') {
      slot.addEventListener('click', () => {
        loadDynamic();
      }, { once: true });
      slot.style.cursor = 'pointer';
      slot.title = 'Click to load content';
    } else if (trigger === 'idle') {
      if ('requestIdleCallback' in window) {
        requestIdleCallback(loadDynamic);
      } else {
        setTimeout(loadDynamic, 100);
      }
    }
  });
}

